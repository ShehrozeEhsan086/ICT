#string method part one

name = "Shehroze Ehsan"

# 1. len() function, counts every case letter.
length = len(name)
print(length)

# 2. lower() method, converts to lower case letters.
print(name.lower())

# 3. upper() method, converts to capital case letters.
print(name.upper())

# 4. title() method, changes first letter capital and others to small.
print(name.title())

# 5. count() method, counts specified letters.
print(name.count("h"))
