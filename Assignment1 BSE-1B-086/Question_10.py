# Takes four digits binary input, converts to decimal equivalent and checks if it is even or odd. 

num = int(input("Enter a 4 digit binary number: "),2) 

if num%2 == 0:
    print(f"The decimal equivalent is {num} and is an even number.")
else:
    print(f"The decimal equivalent is {num} and is an odd number.")

